#!/system/bin/sh
# ============================================================
# SMS Forwarder - 开机自启服务脚本
# KSU/SukiSU 在系统启动完成(boot completed)后执行此脚本
# ============================================================

MODDIR="${0%/*}"

# 后台启动守护进程，脱离前台以免阻塞其他模块加载
nohup sh "${MODDIR}/bin/daemon.sh" > /dev/null 2>&1 &
